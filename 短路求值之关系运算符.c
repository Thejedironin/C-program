#include <stdio.h>
int main()
{
    int a = 5, b = 3;
    (a = 0) && (b = 5);
    //这里a变成了0，此时左边就已经为0，不满足运算&&即两个都为真，所以b=5的运算并没有执行 
	 
    printf("a = %d, b = %d\n", a, b);
    (a = 1) || (b = 5);
    //这里a变成了1，已经完全满足||即至少有一个为真，所以后面的b=5就直接跳过，没有执行 
    
    printf("a = %d, b = %d\n", a, b);
    return 0;
}

