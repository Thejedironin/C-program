#include <stdio.h>
int main(void)
{
	int x = 1;
	printf("%d\n", x); 
	printf("%2d\n", x); 
	printf("%02d\n", x); 
	printf("%.2d", x);
	return 0; 
}
	/*
	%d就是普通的输出了
	%2d是将数字按宽度为2，采用右对齐方式输出，若数据位数不到2位，则左边补空格。
	%02d，和%2d差不多，只不过左边补0
	%.2d 输出整形时最少输出2位，如不够前面以0占位。
	如输出2时变成02。200时只输出200；输出浮点型时（%.2f）小数点后强制2位输出
	*/ 
